const I18nContext = createContext<I18nContextType>({
  language: 'en',
  setLanguage: () => {},
  t: (key: string) => key,
  dir: 'ltr',
  isRTL: false,
  layoutDirection: 'auto',
  setLayoutDirection: () => {}
});
export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    try { const stored = localStorage.getItem(STORAGE_KEY); if (stored === 'ar' || stored === 'en') return stored; } catch {}
    return 'en';
  });
  const [layoutDirection, setLayoutDirectionState] = useState<LayoutDirection>(() => {
    try { const stored = localStorage.getItem(LAYOUT_DIR_KEY); if (stored === 'ltr' || stored === 'rtl' || stored === 'auto') return stored; } catch {}
    return 'auto';
  });
  const effectiveDir: 'ltr' | 'rtl' = layoutDirection === 'auto' ? (language === 'ar' ? 'rtl' : 'ltr') : layoutDirection;
  const isRTL = effectiveDir === 'rtl';
  useEffect(() => {
    document.documentElement.dir = effectiveDir;
    document.documentElement.lang = language;
    if (language === 'ar') { document.body.style.fontFamily = "'Noto Sans Arabic', 'Segoe UI', Tahoma, sans-serif"; }
    else { document.body.style.fontFamily = ''; }
  }, [language, effectiveDir]);
  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    try { localStorage.setItem(STORAGE_KEY, lang); } catch {}
  }, []);
  const setLayoutDirection = useCallback((dir: LayoutDirection) => {
    setLayoutDirectionState(dir);
    try { localStorage.setItem(LAYOUT_DIR_KEY, dir); } catch {}
  }, []);
  const t = useCallback((key: string): string => {
    return translations[language]?.[key] || translations.en[key] || key;
  }, [language]);
  return (
    <I18nContext.Provider value={{ language, setLanguage, t, dir: effectiveDir, isRTL, layoutDirection, setLayoutDirection }}>
      {children}
    </I18nContext.Provider>
  );
}
export function useI18n() {
  return useContext(I18nContext);
}
